=begin
.include?(value) => true or false
.last => returns the last object in range
.max => returns the maximum value in range
.min => returns the minimum value in range
=end

dinos = ["Tyrannosaurus Rex","Triceratops","Stegasaurus","Pternodon"]

puts dinos.include?("birds")
puts dinos.include?("Triceratops")

puts dinos.last
puts dinos.max

puts.dinos.min